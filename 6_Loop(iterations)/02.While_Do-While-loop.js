// while Loop

let a = 5;
while (a <= 15) {
    console.log(`a value is ${a}`);
    a++;
}

const myArr = ['Flesh', 'Batman', 'Superman']
let arr;
while(arr < myArr.length) {
    console.log(`Value is ${myArr}`);
    arr = arr + 1;
}


// Do-while
let score = 1;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);